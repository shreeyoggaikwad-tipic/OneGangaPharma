type Language = 'en' | 'hi' | 'mr';
type TranslationKey = keyof typeof translations.en;

// Get initial language from localStorage, with fallback
const getInitialLanguage = (): Language => {
  if (typeof window === 'undefined') return 'en';
  
  try {
    const saved = localStorage.getItem('sharda-med-language');
    if (saved && ['en', 'hi', 'mr'].includes(saved)) {
      return saved as Language;
    }
  } catch (error) {
    console.warn('Failed to read language from localStorage:', error);
  }
  
  return 'en';
};

export function useTranslation() {
  const [language, setLanguage] = useState<Language>(() => getInitialLanguage());
  const [mounted, setMounted] = useState(false);

  useEffect(() => {
    setMounted(true);
    // Re-check localStorage on mount to ensure consistency
    const savedLang = getInitialLanguage();
    if (savedLang !== language) {
      setLanguage(savedLang);
    }
  }, []);

  useEffect(() => {
    if (mounted) {
      // Save language preference to localStorage whenever it changes
      try {
        localStorage.setItem('sharda-med-language', language);
      } catch (error) {
        console.warn('Failed to save language to localStorage:', error);
      }
    }
  }, [language, mounted]);

  useEffect(() => {
    if (!mounted) return;

    // Listen for storage changes from other tabs
    const handleStorageChange = (e: StorageEvent) => {
      if (e.key === 'sharda-med-language' && e.newValue) {
        const newLang = e.newValue as Language;
        if (['en', 'hi', 'mr'].includes(newLang)) {
          setLanguage(newLang);
        }
      }
    };

    window.addEventListener('storage', handleStorageChange);
    return () => window.removeEventListener('storage', handleStorageChange);
  }, [mounted]);

  const t = (key: TranslationKey): string => {
    if (!mounted) return key; // Return key if not mounted yet
    return translations[language]?.[key] || translations.en[key] || key;
  };

  const changeLanguage = (newLanguage: Language) => {
    if (['en', 'hi', 'mr'].includes(newLanguage)) {
      setLanguage(newLanguage);
    }
  };

  return {
    t,
    language,
    changeLanguage,
    isReady: mounted,
  };
}
